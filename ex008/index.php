    <h1>Funções Aritméticas</h1>
    <ul>
        <li>abs(valor): retorna o valor absoluto de um número.</li>
        <li>base_convert(valor, dessa_base, para_essa_base): retorna o valor convertido de uma base para outra.</li>
        <li>ceil(valor): arredonda para cima</li>
        <li>floor(valor): arredonda para baixo</li>
        <li>round(valor): arredondamento natural</li>
        <li>hypot(cateto01, cateto02): retorna a hipotenusa dos dois catetos passados como parâmetro</li>
        <li>intdiv(): retorna a divisão inteira.</li>
        <li>min(valor1, valor2, ...): retorna o menor valor dentre os passados como parâmetro</li>
        <li>max(valor1, valor2, ...): retorna o maior valor dentre os passados como parâmetro</li>
        <li>pi(): retorna o valor de pi</li>
        <li>pow(valor01, valor02): retorna a potência entre dois valores</li>
    </ul>
